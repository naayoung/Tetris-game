#include <stdio.h>

#define RESET_COLOR "\033[0m"
#define RED "\033[31m"
#define GREEN "\033[32m"
#define YELLOW "\033[33m"
#define BLUE "\033[34m"
#define MAGENTA "\033[35m"

void intro() {
    // 박스를 정의 (모든 줄 길이 20자로 고정)
    const char* boxes[][11] = {
        { // 첫 번째 박스 (빨간색)
            " .----------------.  ",
            "| .--------------. | ",
            "| |  _________   | | ",
            "| | |  _   _  |  | | ",
            "| | |_/ | | \\_|  | | ",
            "| |     | |      | | ",
            "| |    _| |_     | | ",
            "| |   |_____|    | | ",
            "| |              | | ",
            "| '--------------' | ",
            " '----------------'  "
        },
        { // 두 번째 박스 (초록색)
            " .----------------.  ",
            "| .--------------. | ",
            "| |  _________   | | ",
            "| | |_   ___  |  | | ",
            "| |   | |_  \\_|  | | ",
            "| |   |  _|  _   | | ",
            "| |  _| |___/ |  | | ",
            "| | |_________|  | | ",
            "| |              | | ",
            "| '--------------' | ",
            " '----------------'  "
        },
        { // 세 번째 박스 (노란색)
            " .----------------.  ",
            "| .--------------. | ",
            "| |  _________   | | ",
            "| | |_   __ \\    | | ",
            "| |   | |__) |   | | ",
            "| |   |  __ /    | | ",
            "| |  _| |  \\ \\_  | | ",
            "| | |____| |___| | | ",
            "| |              | | ",
            "| '--------------' | ",
            " '----------------'  "
        },
        { // 네 번째 박스 (파란색)
            " .----------------.  ",
            "| .--------------. | ",
            "| |     _____    | | ",
            "| |    |_   _|   | | ",
            "| |      | |     | | ",
            "| |      | |     | | ",
            "| |     _| |_    | | ",
            "| |    |_____|   | | ",
            "| |              | | ",
            "| '--------------' | ",
            " '----------------'  "
        },
        { // 다섯 번째 박스 (자홍색)
            " .----------------.  ",
            "| .--------------. | ",
            "| |    _______   | | ",
            "| |   /  ___  |  | | ",
            "| |  |  (__ \\_|  | | ",
            "| |   '.___`-.   | | ",
            "| |  |`\\____) |  | | ",
            "| |  |_______.'  | | ",
            "| |              | | ",
            "| '--------------' | ",
            " '----------------'  "
        },
    };

    const char* colors[] = { RED, GREEN, YELLOW, BLUE, MAGENTA }; // 색상 정의
    int boxCount = 5; // 박스 개수

    // 각 줄을 가로로 출력
    for (int line = 0; line < 11; line++) { // 박스는 11줄로 구성
        for (int box = 0; box < boxCount; box++) { // 각 박스 순회
            printf("%s%s", colors[box], boxes[box][line]); // 색상 적용 후 출력
        }
        printf("%s\n", RESET_COLOR); // 줄 끝에서 색상 초기화
    }
}